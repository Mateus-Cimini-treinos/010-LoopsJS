// loop simples que defini quantas vezez uma coisa vai acontecer while


var contador = 1  //atribuir a variavel contador com 6
while (contador <= 6) {  // enquanto variavel contador for menor ou igual a 6 fazer:
     console.log("Ola")   // mostrar a mensagem ola no console do NodeJs
     contador++ // acrecenta 1 ao valor da variavel contador a cada loop
} // fecha bloco


