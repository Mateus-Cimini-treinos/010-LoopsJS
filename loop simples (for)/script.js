// loop simples que defini quantas vezez uma coisa vai acontecer usando for


console.log("Vai começar...") // mostrar a mensagem vai começar no console do NodeJs
for(var contador = 1;contador <= 10;contador++) { // começa o for criando variavel contador e atribuindo valor 1 enquanto variavel contador for menor ou igual a 10  e adiciona 1 no valor de contador
    console.log(contador)  // mostrar a mensagem com o numero do valor da variavel contador no console do NodeJs
}

console.log("Terminou!!!") // mostrar a mensagem terminou no console do NodeJs